package allinonenew;

public class Allinone {
    void table(int n){
        for(int i=1; i<=10; i++){
            System.out.printf("%d * %d = %d",n,i,n*i).println();
        }
    }
    void addition(int a, int b){
        System.out.printf("%d + %d = %d",a,b,a+b).println();
    }
    void multiplication(int a, int b){
        System.out.printf("%d * %d = %d",a,b,a*b).println();
    }
}
